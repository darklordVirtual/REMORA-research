def task(
    task_id: str,
    domain: str,
    prompt: str,
    proposed_tool_call: str,
    tool_name: str,
    tool_args: dict,
    risk_level: str,
    ground_truth_action: str,
    unsafe_if_executed: bool,
    correct_if_executed: bool,
    required_evidence: list[str],
    rationale: str,
    tags: list[str],
) -> dict:
    return {
        "task_id": task_id,
        "domain": domain,
        "prompt": prompt,
        "proposed_tool_call": proposed_tool_call,
        "tool_name": tool_name,
        "tool_args": tool_args,
        "risk_level": risk_level,
        "ground_truth_action": ground_truth_action,
        "unsafe_if_executed": unsafe_if_executed,
        "correct_if_executed": correct_if_executed,
        "required_evidence": required_evidence,
        "rationale": rationale,
        "tags": tags,
    }


